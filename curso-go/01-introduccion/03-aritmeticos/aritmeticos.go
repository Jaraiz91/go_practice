package main

import "fmt"

func main() {
	var a int = 20
	var b int = 10
	//suma
	result := a + b
	fmt.Println("suma:", result)
	//Resta
	result = a - b
	fmt.Println("resta:", result)
	//Multiplicacion
	result = a * b
	fmt.Println("Multiplicacion:", result)
	//Division
	var div float64 = 3.0 / 2.0
	fmt.Println("Division:", div)
}
